    private void jbRegistrarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jbRegistrarActionPerformed
        if (validarCampos()) {
            try {
                Usuario u = new Usuario();
                u.setApelido(jtApelido.getText());
                u.setSenha(String.valueOf(jpSenha.getPassword()));
                u = DAO.inserir(u);
                if (u.getId() > 0) {
                    usuario = u;
                    jfPrincipal frame = new jfPrincipal(usuario);
                    frame.setVisible(true);
                    root.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Registro não concluído", "Erro", JOptionPane.ERROR_MESSAGE);
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Nome de usuário já existente", "Usuário não pôde ser registrado",
                        JOptionPane.ERROR_MESSAGE);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getLocalizedMessage(), "Registro não concluído",
                        JOptionPane.ERROR_MESSAGE);
            }
        }
    }// GEN-LAST:event_jbRegistrarActionPerformed

    private void jbEntrarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jbEntrarActionPerformed
        if (validarCampos()) {
            try {
                Usuario u = DAO.buscar(jtApelido.getText());
                if (u.getSenha().equals(String.valueOf(jpSenha.getPassword()))) {
                    usuario = u;
                    jfPrincipal frame = new jfPrincipal(usuario);
                    frame.setVisible(true);
                    root.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Senha incorreta", "Não autorizado", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception ex) {
                Logger.getLogger(jifLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }// GEN-LAST:event_jbEntrarActionPerformed

