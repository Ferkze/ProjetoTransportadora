    private boolean validarCampos() {
        if (jtApelido.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Digite o nome de usuário");
            jtApelido.requestFocus();
            return false;
        }
        if (jpSenha.getPassword().equals("")) {
            JOptionPane.showMessageDialog(this, "Digite a senha da conta");
            jpSenha.requestFocus();
            return false;
        }
        return true;
    }
